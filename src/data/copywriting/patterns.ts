/**
 * 根據格局名稱獲取詳細描述
 */
export function getPatternDescription(patternName: string): PatternDescription {
  return PATTERN_DESCRIPTIONS[patternName] || PATTERN_DESCRIPTIONS['普通格局'];
}

/**
 * 生成格局分析文案
 */
export function generatePatternAnalysis(
  patternName: string,
  status: '成格' | '破格' | '假格'
): string {
  const pattern = getPatternDescription(patternName);

  let analysis = `**${pattern.name}**（${pattern.category}）\n\n`;
  analysis += `${pattern.description}\n\n`;

  if (status === '成格') {
    analysis += `✅ **格局成立**：格局用神有力，可以發揮此格的優勢。\n\n`;
    analysis += `**主要特質**：\n${pattern.characteristics.map(c => `• ${c}`).join('\n')}\n\n`;
    analysis += `**優勢**：\n${pattern.strengths.map(s => `• ${s}`).join('\n')}\n\n`;
    analysis += `**用神**：${pattern.keyGod}\n`;
  } else if (status === '破格') {
    analysis += `⚠️ **格局破損**：用神受傷或被破壞，格局力量減弱。\n\n`;
    analysis += `需要特別注意：\n${pattern.challenges.map(c => `• ${c}`).join('\n')}\n\n`;
    analysis += `**建議**：${pattern.advice}`;
  } else {
    analysis += `🔸 **假格局**：格局條件不完全具備，力量有限。\n\n`;
    analysis += `${pattern.advice}`;
  }

  return analysis;
}
