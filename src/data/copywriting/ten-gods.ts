/**
 * 根據十神獲取完整描述
 */
export function getTenGodDescription(shiShen: ShiShen): TenGodDescription {
  return TEN_GOD_DESCRIPTIONS[shiShen];
}

/**
 * 根據命盤中十神分布生成分析文案
 */
export function generateTenGodAnalysis(
  tenGodCounts: Record<ShiShen, number>,
  dayMaster: string
): string {
  const sorted = Object.entries(tenGodCounts)
    .filter(([_, count]) => count > 0)
    .sort(([, a], [, b]) => b - a);

  if (sorted.length === 0) {
    return '命盤中十神分布均衡，無明顯偏重。';
  }

  const dominant = sorted[0];
  const dominantGod = dominant[0] as ShiShen;
  const dominantDesc = TEN_GOD_DESCRIPTIONS[dominantGod];

  let analysis = `以 ${dayMaster} 為日主，命盤中 ${dominantGod} 最旺（${dominant[1]} 個）。\n\n`;
  analysis += `${dominantDesc.personality}\n\n`;
  analysis += `**性格關鍵詞**：${dominantDesc.keywords.join('、')}\n`;
  analysis += `**適合發展**：${dominantDesc.careers.slice(0, 3).join('、')}\n`;

  if (sorted.length > 1) {
    const secondary = sorted[1];
    analysis += `\n次要影響力為 ${secondary[0]}（${secondary[1]} 個），增添了額外的特質層次。`;
  }

  return analysis;
}
