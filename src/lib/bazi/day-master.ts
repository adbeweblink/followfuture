/**
 * 分析日主天干
 * @param dayGan 日主天干
 * @returns 日主天干分析結果
 */
export function analyzeDayMaster(dayGan: TianGan): DayMasterAnalysis {
  const data = DAY_MASTER_DATA[dayGan];
  return {
    gan: dayGan,
    element: GAN_ELEMENT[dayGan],
    yinYang: GAN_YINYANG[dayGan],
    ...data,
  };
}

/**
 * 取得日主天干簡述
 * @param dayGan 日主天干
 * @returns 簡短描述
 */
export function getDayMasterSummary(dayGan: TianGan): string {
  const data = DAY_MASTER_DATA[dayGan];
  return `${dayGan}${GAN_ELEMENT[dayGan]}（${data.nickname}）：${data.personality.core.split('。')[0]}。`;
}

/**
 * 取得日主天干的核心性格描述
 * @param dayGan 日主天干
 * @returns 核心性格
 */
export function getDayMasterCorePersonality(dayGan: TianGan): string {
  return DAY_MASTER_DATA[dayGan].personality.core;
}

/**
 * 取得日主天干的優缺點
 * @param dayGan 日主天干
 * @returns { strengths: string[], weaknesses: string[] }
 */
export function getDayMasterTraits(dayGan: TianGan): {
  strengths: string[];
  weaknesses: string[];
} {
  const data = DAY_MASTER_DATA[dayGan];
  return {
    strengths: data.personality.strengths,
    weaknesses: data.personality.weaknesses,
  };
}
